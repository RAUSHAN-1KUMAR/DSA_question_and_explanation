/*

Primary:- They are built in data types.(provided by the compiler)
    Ex.-> integer, character, boolean, floating point, double floating point, void, wide character.
    
Derived:- These are those data types which are derived from primary data types.
    Ex.-> fuction, array, pointer, reference, string.
    
User defined:- These data types are defined by the user.
    Ex.-> Class, Structure, Union, Enum, TypeDef.   
 
*/