// 1 bytes = 8 bits

/*
1. Integer data type               -> int a = 3;                size = 4 bytes
2. character data type             -> char u = 'a';             size = 1 bytes
3. boolean data type               -> bool flag = true;         true or false, size = 1 bytes
4. floating point data type        -> float b = 5.3;            till 6 decimal place is allowed, size = 4 bytes
5. double floating point data type -> double b = 3.35981;       till 15 decimal place is allowed, size = 8 bytes
6. void data type                  -> we cannot define it, void means null or nothing
7. wide character data type        -> wchar_t a = 'b';          size = mostly 2 bytes
8. long double data type           -> long double b = 3.3;      till 18 decimal place is allowed, size = 12 bytes.

1. string data type                -> It is not a built.in data type, to store sequence of character we use string
                                      string name = "rohit"; 
*/

/*
Range:-
    1. integer data type                -> in case of 2 bytes -32768 to 32767 and in case of 4 bytes -2147483648 to 2147483647
    2. floating point data type         -> -3.4 X 10^(38) to 3.4 X 10^(38)
    3. double floating point data type  -> -1.7 X 10^(308) to 1.7 X 10^(308)
    4. character data type              -> -128 to 127
*/

