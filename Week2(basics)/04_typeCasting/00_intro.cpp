/*
Type casting in c++ incolves converting a value of one data type to another. There are two types of type casting:

    1. Implicit Type casting:- 
        -- Also known as automatic type conversion, it occurs when the compiler automatically converts one data type into another without any explicit instructions form the programmer. 

                ex:-    int a = 10;
                        double b = a;// 10 as 10.00

    2. Explicit Type Casting:-
        -- Also known as manual type conversion, it occurs when the programmer explicitly instructs the compiler to convert a value from one data type to another.

                ex:-    float a = 10.5;
                        cout << int(a) << endl; // 10.5 as 10
*/